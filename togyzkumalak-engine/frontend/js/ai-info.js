/**
 * Show AI information for selected level
 */
function showAIInfo(level, aiLevelData) {
    const aiInfo = aiLevelData[level];
    if (!aiInfo) return;

    // Desktop version
    const desktopPanel = document.getElementById('aiInfoPanel');
    if (desktopPanel && window.innerWidth > 768) {
        const aiAvatar = document.getElementById('aiAvatar');
        const aiName = document.getElementById('aiName');
        const aiLevelDisplay = document.getElementById('aiLevel');
        const aiEloDisplay = document.getElementById('aiEloDisplay');
        const aiLegend = document.getElementById('aiLegend');

        if (aiAvatar) aiAvatar.textContent = aiInfo.avatar;
        if (aiName) aiName.textContent = aiInfo.name;
        if (aiLevelDisplay) aiLevelDisplay.textContent = aiInfo.level;
        if (aiEloDisplay) aiEloDisplay.textContent = aiInfo.elo;
        if (aiLegend) aiLegend.innerHTML = aiInfo.legend;

        desktopPanel.style.display = 'block';
    }

    // Mobile version
    const mobilePanel = document.getElementById('aiInfoPanelMobile');
    if (mobilePanel && window.innerWidth <= 768) {
        const aiAvatar = document.getElementById('aiAvatarMobile');
        const aiName = document.getElementById('aiNameMobile');
        const aiLevelDisplay = document.getElementById('aiLevelMobile');
        const aiEloDisplay = document.getElementById('aiEloDisplayMobile');
        const aiLegend = document.getElementById('aiLegendMobile');

        if (aiAvatar) aiAvatar.textContent = aiInfo.avatar;
        if (aiName) aiName.textContent = aiInfo.name;
        if (aiLevelDisplay) aiLevelDisplay.textContent = aiInfo.level;
        if (aiEloDisplay) aiEloDisplay.textContent = aiInfo.elo;
        if (aiLegend) aiLegend.innerHTML = aiInfo.legend;

        mobilePanel.style.display = 'block';
    }
}

