/**
 * Show AI information for selected level
 */
function showAIInfo(level, aiLevelData) {
    if (window.innerWidth <= 768) return; // Only on desktop

    // Only show on play mode
    const currentMode = document.querySelector('.main-content:not(.hidden)');
    if (!currentMode || !currentMode.id.includes('play')) {
        hideAIInfo();
        return;
    }

    const aiInfo = aiLevelData[level];
    if (!aiInfo) return;

    const aiPanel = document.getElementById('aiInfoPanel');
    const aiAvatar = document.getElementById('aiAvatar');
    const aiName = document.getElementById('aiName');
    const aiLevelDisplay = document.getElementById('aiLevel');
    const aiEloDisplay = document.getElementById('aiEloDisplay');
    const aiLegend = document.getElementById('aiLegend');

    if (aiAvatar) aiAvatar.textContent = aiInfo.avatar;
    if (aiName) aiName.textContent = aiInfo.name;
    if (aiLevelDisplay) aiLevelDisplay.textContent = aiInfo.level;
    if (aiEloDisplay) aiEloDisplay.textContent = aiInfo.elo;
    if (aiLegend) aiLegend.textContent = aiInfo.legend;

    if (aiPanel) {
        aiPanel.style.display = 'block';
    }
}

/**
 * Hide AI info panel
 */
function hideAIInfo() {
    const aiPanel = document.getElementById('aiInfoPanel');
    if (aiPanel) {
        aiPanel.style.display = 'none';
    }
}

// Hide AI panel when game starts or mode changes
document.addEventListener('DOMContentLoaded', function() {
    // Hide when game starts
    const setupPanel = document.getElementById('setupPanel');
    if (setupPanel) {
        const observer = new MutationObserver(function(mutations) {
            mutations.forEach(function(mutation) {
                if (mutation.type === 'attributes' && mutation.attributeName === 'class') {
                    if (setupPanel.classList.contains('hidden')) {
                        hideAIInfo();
                    }
                }
            });
        });
        observer.observe(setupPanel, { attributes: true, attributeFilter: ['class'] });
    }

    // Hide when switching modes
    const modeTabs = document.querySelectorAll('.mode-tab');
    modeTabs.forEach(tab => {
        tab.addEventListener('click', function() {
            if (!this.classList.contains('active') || !this.dataset.mode.includes('play')) {
                hideAIInfo();
            }
        });
    });
});

